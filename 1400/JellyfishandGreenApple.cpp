void solve() {
    int n, m; cin >> n >> m;

    int ans = 0;
    bool ok = 0;
    for (int i = 0; i < 64; i++) {
        n %= m;
        if (n == 0) {
            ok = 1;
            break;
        }
        ans += n;
        n <<= 1;
    }
    cout << (ok ? ans : -1) << nl;
}

