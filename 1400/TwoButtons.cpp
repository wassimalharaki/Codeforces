void solve() {
    int n, m; cin >> n >> m;

    int a = 0;
    while (n < m)
        n <<= 1, a++;
    int b = n - m;
    for (int i = a; i >= 0; i--) {
        int c = b / (1 << i);
        b -= c * (1 << i);
        a += c;
    }
    cout << a;
}

int32_t main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0);

    int T = 1;
    // cin >> T;
    while (T--) solve();

    return 0;
}