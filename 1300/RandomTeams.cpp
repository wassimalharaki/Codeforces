void solve() {
    int n, m; cin >> n >> m;
    int per = n / m;
    int ones = n % m;
    int min = per * ((per + 1) * ones + (per - 1) * (m - ones)) / 2;
    int max = (n - m + 1) * (n - m) / 2;
    cout << min << " " << max; 
}

